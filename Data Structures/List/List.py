#A list is an ordered, changeable collection.
#We use it when you want to store multiple values and may need to add, remove, or update them later.

# Important properties:
    # Ordered: yes.
    # Mutable: yes.
    # Duplicates allowed: yes.
    # Indexing: yes

# When to use a list:
    # When order matters.
    # When we may change the data.
    # When duplicates are allowed.
    # When we need to loop through values.


# Imp List Pattern

# Pattern 1: Traverse
# for x in lst:
#     print(x)

# Pattern 2: Accumulator (SUM / TOTAL)
# total = 0
# for x in lst:
#     total += x

# Pattern 3: Counter
# count = 0
# for x in lst:
#     if condition:
#         count += 1

# Pattern 4: Filter
# for x in lst:
#     if condition:
#         print(x)

# Pattern 5: Search
# found = False
# for x in lst:
#     if x == target:
#         found = True

